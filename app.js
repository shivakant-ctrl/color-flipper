const characters = '0123456789ABCDEF'

let colorChangeElement = document.getElementById("change--color")
let mainButton = document.getElementsByClassName("main--button")[0]

function randomHexColor() {
  let randomHexValue = "#"
  for(let i = 0; i < 6; i++) {
    randomHexValue = randomHexValue + characters[Math.floor(Math.random() * characters.length)]
  }
  return randomHexValue
}

function changeColorToHex() {
  let randomHexValue = randomHexColor()
  colorChangeElement.innerHTML = randomHexValue
  colorChangeElement.style.color = randomHexValue
  document.getElementById("main").style.backgroundColor = randomHexValue
}

function changeColorToSimple() {
  let randomColorIndex = Math.floor(Math.random() * CSS_COLOR_NAMES.length)
  colorChangeElement.innerHTML = CSS_COLOR_NAMES[randomColorIndex]
  colorChangeElement.style.color = CSS_COLOR_NAMES[randomColorIndex]
  document.getElementById("main").style.backgroundColor = CSS_COLOR_NAMES[randomColorIndex]
}

function simpleColors() {
  changeColorToSimple()
  mainButton.setAttribute("onclick", "changeColorToSimple()")
}

function hexColors() {
  changeColorToHex()
  mainButton.setAttribute("onclick", "changeColorToHex()")
}